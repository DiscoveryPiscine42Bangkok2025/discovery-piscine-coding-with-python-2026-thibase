#!/usr/bin/env python
import sys

# เช็คว่ามีการส่ง Argument มาไหม (ถ้า len > 1 แปลว่ามี)
if len(sys.argv) > 1:
    print("none")
    sys.exit()

# เริ่มต้นตัวแปรสำหรับแม่สูตรคูณ (แม่ 0 ถึง 10)
i = 0
while i <= 10:
    # พิมพ์หัวข้อ "Table de X:" แบบไม่ขึ้นบรรทัดใหม่ (ใช้ end='')
    print("Table de " + str(i) + ":", end='')
    
    # เริ่มต้นตัวแปรตัวคูณ (x0 ถึง x10)
    j = 0
    while j <= 10:
        # พิมพ์ตัวเลขผลคูณ เว้นวรรคข้างหน้า
        print(" " + str(i * j), end='')
        j += 1
    
    # พอจบลูปใน (ครบ 0-10) ให้สั่ง print() เปล่าๆ เพื่อขึ้นบรรทัดใหม่เตรียมแม่ต่อไป
    print()
    i += 1